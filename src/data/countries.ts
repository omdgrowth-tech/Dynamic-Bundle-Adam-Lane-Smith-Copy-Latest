// Helper functions
export const getCountryByCode = (code: string): Country | undefined => {
  return COUNTRIES.find(country => country.code === code);
};

export const getStatesForCountry = (countryCode: string): State[] => {
  const country = getCountryByCode(countryCode);
  return country?.states || [];
};

export const requiresState = (countryCode: string): boolean => {
  const country = getCountryByCode(countryCode);
  return country?.requiresState || false;
};

export const getStateLabel = (countryCode: string): string => {
  const country = getCountryByCode(countryCode);
  return country?.stateLabel || "State/Province";
};

export const getZipLabel = (countryCode: string): string => {
  const country = getCountryByCode(countryCode);
  return country?.zipLabel || "Postal Code";
};

export const requiresZip = (countryCode: string): boolean => {
  const country = getCountryByCode(countryCode);
  return country?.zipRequired !== false; // Default to true if not specified
};
